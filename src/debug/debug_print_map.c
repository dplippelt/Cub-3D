#include "debug.h"

void	debug_print_map(t_map *m)
{
	size_t	line;

	line = 0;
	printf("\n======================= FILE CONTENT =======================\n");
	while (m->map[line])
		printf("%s\n", m->map[line++]);
	printf("============================================================\n\n");
}
